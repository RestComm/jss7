package org.mobicents.protocols.ss7.tools.traceparser;

/**
 * 
 * @author sergey vetyutnev
 * 
 */
public class TraceReaderException extends Exception {

	public TraceReaderException() {
	}

	public TraceReaderException(String message) {
		super(message);
	}

	public TraceReaderException(Throwable cause) {
		super(cause);
	}

	public TraceReaderException(String message, Throwable cause) {
		super(message, cause);
	}

}

